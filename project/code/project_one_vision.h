#ifndef _PROJECT_ONE_VISION_H_
#define _PROJECT_ONE_VISION_H_

#include "zf_common_typedef.h"

/*********************************************************************************************************************
 * 文件名称：project_one_vision.h
 * 文件功能：科目一“绕桩区”摄像头导航模块头文件
 *
 * 设计目标：
 * 1. 当 GPS 判断车辆进入绕桩区后，启用摄像头导航。
 * 2. 摄像头模块负责：
 *      - 自动曝光
 *      - 自动阈值
 *      - 自动二值化
 *      - 锥桶检测
 *      - 间隙中心计算
 *      - 输出视觉相对 yaw
 * 3. 控制层不直接关心图像处理细节，只需要读取：
 *      g_project_one_yaw
 *
 * 推荐控制方式：
 *      Target_Yaw_Angle = Vision_Enter_Yaw + g_project_one_yaw;
 *
 * 其中：
 *      Vision_Enter_Yaw
 *          进入绕桩区时记录的当前车体航向角
 *
 *      g_project_one_yaw
 *          当前视觉模块输出的“相对偏航角”
 *
 *********************************************************************************************************************/


//-------------------------------------------------------------------------------------------------------------------
// 图像尺寸定义
// 必须与摄像头当前配置保持一致
//-------------------------------------------------------------------------------------------------------------------
#define PROJECT_ONE_IMAGE_W    (188)
#define PROJECT_ONE_IMAGE_H    (120)


//-------------------------------------------------------------------------------------------------------------------
// 视觉状态枚举
//-------------------------------------------------------------------------------------------------------------------
typedef enum
{
    PROJECT_ONE_VISION_DISABLE = 0,   // 视觉模块未启用
    PROJECT_ONE_VISION_SEARCH,        // 正常搜索/跟踪锥桶
    PROJECT_ONE_VISION_SINGLE_CONE,   // 只看到一个锥桶，执行单桶绕行
    PROJECT_ONE_VISION_DOUBLE_CONE,   // 同时看到两个锥桶，执行穿间隙
    PROJECT_ONE_VISION_LOST,          // 暂时丢失锥桶，短时保持上一帧目标
    PROJECT_ONE_VISION_SCAN           // 持续丢失锥桶，进入主动摆扫搜索
} project_one_vision_state_enum;


//-------------------------------------------------------------------------------------------------------------------
// 全局变量说明
//-------------------------------------------------------------------------------------------------------------------

/*********************************************************************************************************************
 * 变量名称：g_project_one_binary_image
 * 变量类型：uint8[120][188]
 * 变量含义：
 *      视觉模块内部调用 auto_process() 后得到的二值图
 *
 * 使用说明：
 *      1. 这个数组主要用于调试显示和辅助分析
 *      2. 黑白极性是否和现场一致，需要你们上车后确认
 *      3. 如果想在屏幕上显示二值图，可以直接用它
 *
 *********************************************************************************************************************/
extern uint8 g_project_one_binary_image[PROJECT_ONE_IMAGE_H][PROJECT_ONE_IMAGE_W];


/*********************************************************************************************************************
 * 变量名称：g_project_one_target_x
 * 变量类型：int
 * 变量含义：
 *      当前帧视觉模块计算得到的目标列坐标
 *
 * 解释：
 *      可以理解为“当前希望车辆朝向的图像横向目标点”
 *      例如：
 *          - 双桶时，目标点是安全间隙中心
 *          - 单桶时，目标点是远离锥桶的一侧安全边界
 *
 *********************************************************************************************************************/
extern int g_project_one_target_x;


/*********************************************************************************************************************
 * 变量名称：g_project_one_error
 * 变量类型：int
 * 变量含义：
 *      当前视觉横向误差
 *
 * 计算方式：
 *      g_project_one_error = g_project_one_target_x - 图像中心列
 *
 * 含义说明：
 *      > 0 目标在图像中心右侧，车需要向右修正
 *      < 0 目标在图像中心左侧，车需要向左修正
 *
 *********************************************************************************************************************/
extern int g_project_one_error;


/*********************************************************************************************************************
 * 变量名称：g_project_one_yaw
 * 变量类型：float
 * 变量含义：
 *      当前视觉模块输出的“相对 yaw 角”
 *
 * 非常重要：
 *      这个变量不是绝对航向角，而是相对于进入绕桩区时基准航向的修正量
 *
 * 正确使用方式：
 *      Target_Yaw_Angle = Vision_Enter_Yaw + g_project_one_yaw;
 *
 * 不要写成：
 *      Vision_Enter_Yaw += g_project_one_yaw;
 *
 *********************************************************************************************************************/
extern float g_project_one_yaw;


/*********************************************************************************************************************
 * 变量名称：g_project_one_enable_flag
 * 变量类型：uint8
 * 变量含义：
 *      视觉模块使能标志
 *
 * 取值说明：
 *      0：未启用
 *      1：已启用
 *
 *********************************************************************************************************************/
extern uint8 g_project_one_enable_flag;


/*********************************************************************************************************************
 * 变量名称：g_project_one_block_count
 * 变量类型：uint8
 * 变量含义：
 *      当前帧识别到的障碍块数量
 *
 * 说明：
 *      这里的“障碍块”可以理解为锥桶在二值图中的一团投影区域
 *
 *********************************************************************************************************************/
extern uint8 g_project_one_block_count;


/*********************************************************************************************************************
 * 变量名称：g_project_one_state
 * 变量类型：uint8
 * 变量含义：
 *      当前视觉状态
 *
 * 对应枚举：
 *      project_one_vision_state_enum
 *
 *********************************************************************************************************************/
extern uint8 g_project_one_state;


/*********************************************************************************************************************
 * 变量名称：g_project_one_lost_frame_count
 * 变量类型：uint8
 * 变量含义：
 *      连续丢失锥桶的帧数
 *
 * 作用：
 *      用于区分：
 *          - 短时丢失：继续保持上一帧目标
 *          - 持续丢失：进入主动摆扫搜索模式
 *
 *********************************************************************************************************************/
extern uint8 g_project_one_lost_frame_count;


//-------------------------------------------------------------------------------------------------------------------
// 函数声明
//-------------------------------------------------------------------------------------------------------------------

/*********************************************************************************************************************
 * 函数名称：project_one_vision_init
 * 函数功能：初始化科目一摄像头绕桩模块
 *
 * 调用时机：
 *      系统初始化阶段调用一次即可
 *
 * 内部完成：
 *      1. 初始化模块状态
 *      2. 清空误差和输出量
 *      3. 初始化 auto_binary 模块
 *
 * 推荐调用位置：
 *      摄像头初始化完成后调用
 *
 * 示例：
 *      mt9v03x_init();
 *      project_one_vision_init();
 *
 *********************************************************************************************************************/
void project_one_vision_init(void);


/*********************************************************************************************************************
 * 函数名称：project_one_vision_enable
 * 函数功能：启用绕桩视觉模块
 *
 * 调用时机：
 *      当 GPS 判断“距离绕桩区 < 4m”后调用
 *
 * 作用：
 *      1. 打开视觉处理流程
 *      2. 允许 project_one_vision_run() 正常输出视觉 yaw
 *
 * 示例：
 *      if(gps_distance_less_than_4m())
 *      {
 *          Vision_Enter_Yaw = current_yaw;
 *          project_one_vision_enable();
 *      }
 *
 *********************************************************************************************************************/
void project_one_vision_enable(void);


/*********************************************************************************************************************
 * 函数名称：project_one_vision_disable
 * 函数功能：关闭绕桩视觉模块
 *
 * 调用时机：
 *      绕桩结束后，或者切换到其他模式时调用
 *
 *********************************************************************************************************************/
void project_one_vision_disable(void);


/*********************************************************************************************************************
 * 函数名称：project_one_vision_run
 * 函数功能：每帧执行一次科目一绕桩视觉处理
 *
 * 输入参数：
 *      image
 *          摄像头采集得到的灰度图，一般直接传入 mt9v03x_image
 *
 * 处理流程：
 *      1. 自动曝光 + 自动阈值 + 自动二值化
 *      2. 识别锥桶块
 *      3. 若识别到：
 *             - 单桶：绕行
 *             - 双桶：穿间隙
 *      4. 若未识别到：
 *             - 短暂丢失：保持上一帧方向
 *             - 持续丢失：输出主动搜索 yaw
 *
 * 输出结果：
 *      g_project_one_target_x
 *      g_project_one_error
 *      g_project_one_yaw
 *      g_project_one_state
 *
 * 推荐调用方式：
 *      if(mt9v03x_finish_flag)
 *      {
 *          mt9v03x_finish_flag = 0;
 *          project_one_vision_run(mt9v03x_image);
 *      }
 *
 *********************************************************************************************************************/
void project_one_vision_run(const uint8 image[PROJECT_ONE_IMAGE_H][PROJECT_ONE_IMAGE_W]);

#endif