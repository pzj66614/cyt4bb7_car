#ifndef _AUTO_BINARY_H_
#define _AUTO_BINARY_H_

#include "zf_common_typedef.h"

/*********************************************************************************************************************
 * 文件功能说明：
 * 本文件配合 auto_binary.c 使用，用于实现以下三个功能：
 *
 * 1. 自动曝光
 *    根据当前图像整体亮度，自动调用摄像头曝光设置函数，
 *    让图像不要过亮或过暗。
 *
 * 2. 自动阈值
 *    根据当前图像内容，自动计算适合本帧图像的二值化阈值。
 *
 * 3. 自动二值化
 *    使用自动计算得到的阈值，把灰度图转换成二值图。
 *
 *
 * 适用场景：
 * 本模块适用于 MT9V03X / MT9V034 灰度摄像头图像处理流程，
 * 尤其适合智能车比赛中的：
 * - 巡线
 * - 中线提取
 * - 边界搜索
 * - 赛道识别
 *
 *
 * 使用方式概览：
 *
 * 第一步：在 main.c 或其他源文件中包含本头文件
 *      #include "auto_binary.h"
 *
 * 第二步：定义二值图数组
 *      uint8 binary_image[120][188];
 *
 * 第三步：系统初始化时调用一次
 *      auto_process_init();
 *
 * 第四步：每当摄像头采集完一帧图像后调用
 *      auto_process(mt9v03x_image, binary_image);
 *
 * 第五步：调用完成后，直接使用 binary_image 做巡线/找中线即可
 *
 *
 * 典型调用示例：
 *
 * if(mt9v03x_finish_flag)
 * {
 *     mt9v03x_finish_flag = 0;
 *
 *     // 自动曝光 + 自动阈值 + 自动二值化
 *     auto_process(mt9v03x_image, binary_image);
 *
 *     // 后续直接使用 binary_image 进行巡线处理
 *     search_line(binary_image);
 * }
 *
 *
 * 调用 auto_process() 后会自动更新以下全局变量：
 * 1. g_auto_threshold
 *      当前帧计算得到的二值化阈值
 *
 * 2. g_auto_exposure
 *      当前正在使用的曝光值
 *
 *
 * 调试建议：
 * 可以把 g_auto_threshold 和 g_auto_exposure 显示到 IPS114 屏幕上，
 * 方便观察自动调节是否正常。
 *
 * 示例：
 *      ips114_show_int(0, 0, g_auto_threshold, 3);
 *      ips114_show_int(0, 16, g_auto_exposure, 4);
 *
 ********************************************************************************************************************/


//-------------------------------------------------------------------------------------------------------------------
// 图像尺寸定义
// 这里必须和摄像头实际输出分辨率一致
// 当前工程默认是 MT9V03X_W = 188, MT9V03X_H = 120
//-------------------------------------------------------------------------------------------------------------------
#define AUTO_IMAGE_W    (188)   // 图像宽度
#define AUTO_IMAGE_H    (120)   // 图像高度


//-------------------------------------------------------------------------------------------------------------------
// 全局变量说明
//-------------------------------------------------------------------------------------------------------------------

/*********************************************************************************************************************
 * 变量名称      g_auto_threshold
 * 变量类型      uint8
 * 作用说明      保存当前帧自动计算得到的阈值
 *
 * 使用方式：
 * 在调用 auto_process() 后，这个变量会被自动更新。
 * 如果你想知道当前二值化使用的阈值是多少，可以直接查看它。
 *
 * 例如：
 *      ips114_show_int(0, 0, g_auto_threshold, 3);
 *
 *********************************************************************************************************************/
extern uint8  g_auto_threshold;


/*********************************************************************************************************************
 * 变量名称      g_auto_exposure
 * 变量类型      uint16
 * 作用说明      保存当前自动曝光控制使用的曝光值
 *
 * 使用方式：
 * 在调用 auto_process() 后，这个变量可能会被自动调整。
 * 如果你想知道当前摄像头曝光值是多少，可以直接查看它。
 *
 * 例如：
 *      ips114_show_int(0, 16, g_auto_exposure, 4);
 *
 *********************************************************************************************************************/
extern uint16 g_auto_exposure;


//-------------------------------------------------------------------------------------------------------------------
// 函数声明
//-------------------------------------------------------------------------------------------------------------------

/*********************************************************************************************************************
 * 函数名称      auto_process_init
 * 函数功能      自动曝光/自动阈值模块初始化
 *
 * 参数说明      无
 * 返回值        无
 *
 * 使用时机：
 * 在系统初始化阶段调用一次即可。
 * 一般放在 main() 初始化部分，摄像头初始化完成后调用。
 *
 * 调用示例：
 *      mt9v03x_init();
 *      auto_process_init();
 *
 * 注意事项：
 * 1. 本函数只需要调用一次，不需要每帧都调用。
 * 2. 本函数内部会设置初始曝光值。
 * 3. 建议在摄像头初始化成功后再调用。
 *
 *********************************************************************************************************************/
void auto_process_init(void);


/*********************************************************************************************************************
 * 函数名称      auto_process
 * 函数功能      对当前灰度图执行一整套自动处理流程
 *
 * 处理内容包括：
 * 1. 自动调整曝光
 * 2. 自动计算阈值
 * 3. 根据阈值完成二值化
 *
 * 参数说明：
 *      image
 *          输入图像，类型为灰度图
 *          一般直接传入摄像头采集得到的 mt9v03x_image
 *
 *      binary
 *          输出图像，类型为二值图
 *          本函数执行完成后，binary 中保存二值化结果
 *
 * 返回值：
 *      无
 *
 * 最常见调用方式：
 *      auto_process(mt9v03x_image, binary_image);
 *
 * 推荐调用位置：
 *      在检测到一帧图像采集完成后调用
 *
 * 推荐调用示例：
 *
 *      if(mt9v03x_finish_flag)
 *      {
 *          mt9v03x_finish_flag = 0;
 *
 *          auto_process(mt9v03x_image, binary_image);
 *
 *          // 此时 binary_image 已经可用于巡线
 *      }
 *
 * 调用完成后你可以直接使用：
 *      1. binary            -> 二值图结果
 *      2. g_auto_threshold  -> 当前阈值
 *      3. g_auto_exposure   -> 当前曝光
 *
 * 注意事项：
 * 1. image 和 binary 的尺寸必须和宏定义 AUTO_IMAGE_W / AUTO_IMAGE_H 对应一致
 * 2. binary 数组必须由外部提前定义好
 * 3. 本函数建议每帧只调用一次
 *
 *********************************************************************************************************************/
void auto_process(const uint8 image[AUTO_IMAGE_H][AUTO_IMAGE_W],
                  uint8 binary[AUTO_IMAGE_H][AUTO_IMAGE_W]);

#endif